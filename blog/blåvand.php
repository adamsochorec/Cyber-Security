          approaching the sea shore is time to head ??south??, along the
          ??403km?? long west coast, which on the south meets the German border, and
          on the north, in Skagen, flows into the Baltic sea. The beach sound of
          waves blends with the screaming of seagulls and kids playing with old
          WWII bunkers, build by the ??Nazis?? in ?? 1945??. I soon figure out
