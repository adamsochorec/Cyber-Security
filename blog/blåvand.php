            Their exterior is usually covered by graffiti and some of them
          have additional Grotesk metal construction, shaping them like a horse
          or giraffe. As I pass several bunkers, occupied with screaming
          children, climbing up on the roof, hiding in the interior and 
          former machine gun nests, I realize the point. WWII bunkers, built
          with the purpose to make killing people more efficient, with fewer losses,
          are now toys and playgrounds! The wraith spirit is gone, devaluated
          , and replaced by human creativity and the attitude of youths. It is hard to
          imagine something more embarrassing could happen to those constructions.
